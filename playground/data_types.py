# Python Data Types Example
age = 30 # Integer
cash_on_hand = 20.50 # Float
name = 'Alice' # String
favorite_colors = ['blue', 'green', 'red'] # List
immutable_numbers = (1, 2, 3) # Tuple
contact_info = {'email': 'alice@example.com', 'phone': '123-456-7890'} # Dictionary
is_student = True # Boolean

# Print the values
print(f'Age: {age}, Cash on Hand: {cash_on_hand}, Name: {name}')
print(f'Favorite Colors: {favorite_colors}, Immutable Numbers: {immutable_numbers}, Contact Info: {contact_info}')
print(f'Is a student: {is_student}')
