
# List creation
fruits = ['apple', 'banana', 'cherry', 'date']

# Accessing elements
first_fruit = fruits[0]  # Access the first element
last_fruit = fruits[-1]  # Access the last element

# List slicing
first_two_fruits = fruits[:2]  # Get the first two elements
last_two_fruits = fruits[-2:]  # Get the last two elements

# Common list methods
fruits.append('elderberry')  # Add an element to the end of the list
fruits.remove('banana')  # Remove an element from the list

# Display results
print('Original list:', fruits)
print('First fruit:', first_fruit)
print('Last fruit:', last_fruit)
print('First two fruits:', first_two_fruits)
print('Last two fruits:', last_two_fruits)
