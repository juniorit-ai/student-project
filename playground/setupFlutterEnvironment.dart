# Install Flutter SDK
sudo snap install flutter --classic

# Verify Installation
flutter doctor

# Install Android Studio
sudo snap install android-studio --classic

# Configure Android Studio for Flutter
# Open Android Studio > Configure > Plugins > Install 'Flutter' plugin > Restart IDE

# Install VS Code (if preferred)
sudo snap install code --classic

# Configure VS Code for Flutter
# Open VS Code > Extensions > Search 'Flutter' > Install > Reload

# Verify Configuration
flutter doctor --android-licenses