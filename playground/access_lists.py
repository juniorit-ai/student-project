my_list = [1, 2, 3, 4, 5]
# Accessing elements using indexing
print(my_list[0])  # Accesses the first element
print(my_list[4])  # Accesses the fifth element
# Accessing elements using slicing
print(my_list[1:3])  # Accesses elements from index 1 to 2
print(my_list[:2])  # Accesses elements from the start to index 1
print(my_list[3:])  # Accesses elements from index 3 to the end