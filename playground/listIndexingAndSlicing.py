
# Example list
fruits = ['apple', 'banana', 'cherry', 'date', 'elderberry']

# Accessing individual elements using indexing
first_fruit = fruits[0]
second_fruit = fruits[1]
last_fruit = fruits[-1]

# Accessing a range of elements using slicing
first_three_fruits = fruits[0:3]
last_two_fruits = fruits[-2:]

# Modifying elements using indexing
fruits[1] = 'blueberry'

# Output the results
print('First fruit:', first_fruit)
print('Second fruit:', second_fruit)
print('Last fruit:', last_fruit)
print('First three fruits:', first_three_fruits)
print('Last two fruits:', last_two_fruits)
print('Modified list:', fruits)
