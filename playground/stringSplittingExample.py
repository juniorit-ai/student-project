
text = "Hello, world! This is a Python example."
# Split by comma
split_by_comma = text.split(',')
print('Split by comma:', split_by_comma)

# Split by space
split_by_space = text.split()
print('Split by space:', split_by_space)

# Split by new line
text_with_new_line = "Hello\nworld\nThis is a Python example."
split_by_new_line = text_with_new_line.split('\n')
print('Split by new line:', split_by_new_line)
